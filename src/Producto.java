public class Producto {
    //variables
    String id;
    String nombre;
    String primerApellido;
    String segundoApellido;
    String lote;
    //metodo constructor

    public Producto(String id, String nombre, String primerApellido, String segundoApellido, String lote) {
        this.id = id;
        this.nombre = nombre;
        this.primerApellido = primerApellido;
        this.segundoApellido = segundoApellido;
        this.lote = lote;
    }
}
