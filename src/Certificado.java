public class Certificado {
    //variables
    String id;
    String calidad;
    String anomalias;
    String idEnsayo;
    //metodo constructor

    public Certificado(String id, String calidad, String anomalias, String idEnsayo) {
        this.id = id;
        this.calidad = calidad;
        this.anomalias = anomalias;
        this.idEnsayo = idEnsayo;
    }
}
