public class Laboratorio {
    //variables
    String id;
    String nombre;
    String primerApellido;
    String segundoApellido;
    String ciudad;
    String direccion;
    String telefono;
    //metodo constructor


    public Laboratorio(String id, String nombre, String primerApellido, String segundoApellido, String ciudad, String direccion, String telefono) {
        this.id = id;
        this.nombre = nombre;
        this.primerApellido = primerApellido;
        this.segundoApellido = segundoApellido;
        this.ciudad = ciudad;
        this.direccion = direccion;
        this.telefono = telefono;
    }
}
